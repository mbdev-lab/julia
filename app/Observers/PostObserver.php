    /**
     * Handle the Post "created" event.
     *
     * @param  \App\Models\Post  $post
     * @return void
     */
    public function created(Post $post)
    {
        $cacheKey = 'post_' . md5($post->user_id);
        $cached = Cache::get($cacheKey);

        if(!empty($cached) && $cached instanceof Collection) {
            $cached->push($post);
            Cache::put($cacheKey, $cached, Carbon::now()->addMinutes(60));
        }
    }

